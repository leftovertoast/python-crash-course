# Exercise 3-8 Seeing the World
# Learning Objective: Utilize sorted(), reverse(), and sort() methods on a list.

placesToVisit = ['Zimbabwe', 'Georgia', 'Anarctica', 'Puerto Rico', 'Cuba']
print(placesToVisit)
print(sorted(placesToVisit))
print(placesToVisit)
placesToVisit.reverse()
print(placesToVisit)
placesToVisit.reverse()
print(placesToVisit)
placesToVisit.sort()
print(placesToVisit)
placesToVisit.sort(reverse=True)
print(placesToVisit)