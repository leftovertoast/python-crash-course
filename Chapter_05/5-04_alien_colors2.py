# Runs if block:
alien_color = 'green'

if (alien_color == 'green'):
    print("\nPlayer just scored 5 points!")
else: 
	print("\nPlayer just scored 10 points!")

# Runs else block:

if (alien_color != 'green'):
    print("\nPlayer just socred 10 points!")
else:
	print("\nPlayer just scored 5 points!")
